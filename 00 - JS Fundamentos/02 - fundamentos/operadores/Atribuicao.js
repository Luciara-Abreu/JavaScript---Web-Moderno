// Atribuição

const a = 7; // = é um simbolo de atribuição
let b = 5;
console.log('b = b(5) + a(7) ---------------------- aditiva');
//Atribuição aditiva - com soma
b += a; 
console.log(b);

console.log('b = b(12) - a(7) ---------------------- subtrativa ');
//Atribuição subtrativa - com soma
b -= a;
console.log(b);

console.log('b = b(5) * a(7) ---------------------- multiplicativa ');
b *= a;
console.log(b);

console.log('b = b(35) / a(7) ---------------------- divisiva ');
b /= a;
console.log(b);

console.log('b = b(5) % a(7) ---------------------- modular ');
// o resto da divisão é atribuido a variável b
b %= a;
console.log(b);

